const findSmallestInt = arr => Math.min(...arr);

console.log(findSmallestInt([78, 56, 232, 12, 8])); // 8
console.log(findSmallestInt([78, 56, 232, 12, 18])); // 12
console.log(findSmallestInt([78, 56, 232, 412, 228])); // 56